package CAMSv2;
/**
 * The {@code Role} enum represents the roles of users in the CAMSv2 system.
 * Users can have one of the following roles: STAFF, STUDENT, or CAMP_COMMITTEE_MEMBER.
 */

public enum Role {
    /**
     * The role assigned to staff members.
     */
    STAFF,
    /**
     * The role assigned to students.
     */

    STUDENT,
    /**
     * The role assigned to camp committee members.
     */
    CAMP_COMMITTEE_MEMBER
}

